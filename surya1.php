            <p class="pose-name">2: Raised arms pose – Hastauttanasana</p>
            <label style="font-size:21px;color:yellow;font-weight:bold;margin-left:20px;">Execution:</label><br>
              <ul><br>
                <li>Make the salutation: ॐ रवये नमः (om ravaye namaḥ.)</li>
                <li>Breath in and lift your arms up and back.</li>
                <li>Make effort to stretch your whole body starting from the heels up to the finger tips.</li>
                <li>Instead of bending backwards, reach up with fingers.</li>
              </ul>
