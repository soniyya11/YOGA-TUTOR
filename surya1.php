<h3 class="tre">Precautions While Doing Salutations</h3>
<ol class="adv">
<li>While doing 2nd posture, you’re required to take care of the neck, making sure that the neck should not float back your arms, as it can lead to a serious neck injury.</li>
<li>In the 3rd posture, bending down randomly or directly without stretching is a strict no-no as you will get problems in the back muscles.</li>
<li>Never put your weight on your fingers, and instead, put pressure on your palms while performing the 4th.</li>
<li>Always see to it that the abdomen should not be attached to the ground and you should not lift your hips too high whilst practicing the 5th yoga posture as it can put unwanted force on the lower back resulting in injury.</li>
<li>Keep in mind these Pros and Cons of Surya Namaskar or Sun Salutation. Whilst performing the Sun Salutation, we would recommend you to do this asana under the able guidance of a trained yoga teacher or instructor who will demonstrate and teach you the know-how of this asana accurately.</li>
</ol>
