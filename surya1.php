            <p class="pose-name">6: Salute with eight parts or points – Ashtanga Namaskara</p>
            <label style="font-size:21px;color:yellow;font-weight:bold;margin-left:20px;">Execution:</label><br>
              <ul><br>
                <li> Make the salutation: ॐ पूष्णे नमः (om pūṣṇe namaḥ).</li>
                <li>Gradually bring your knees down to the floor then exhale.</li>
                <li>Slightly take your hips back and slide forward.</li>
                <li>Relax your chest and chin on the ground.</li>
                <li>Elevate your posterior a little bit.</li>
              </ul>
