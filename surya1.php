            <p class="pose-name">8: Mountain pose – Parvatasana</p>
            <label style="font-size:21px;color:yellow;font-weight:bold;margin-left:20px;">Execution:</label><br>
              <ul><br>
                <li>Make the salutation: ॐ मरीचये नमः (om marīcaye namaḥ).</li>
