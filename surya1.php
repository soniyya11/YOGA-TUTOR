            <p class="pose-name">1: Prayer pose – Pranamasana</p>
            <label style="font-size:21px;color:yellow;font-weight:bold;margin-left:20px;">Execution:</label><br>
              <ul><br>
                <li>Make the salutation: ॐ मित्राय नमः (om mitrāya namaḥ)</li>
                <li >Stand at the end of mat,keep your feet together and distribute your weight on both feet equally.</li>
                <li>Open your chest and just relax your shoulders.</li>
                <li>Breathe in and lift both your arms up from the sides.</li>
                <li>Exhale and bring your palms together in a prayer position in front of your chest.</li>
              </ul>
        </div>
